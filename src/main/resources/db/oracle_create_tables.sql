    UPDATED_AT    TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL ,
    PRIMARY KEY (ID),
    FOREIGN KEY (API_CONFIG_ID) REFERENCES API_CONFIG (ID) ON DELETE CASCADE,
