-- API_ACCOUNT表
CREATE TABLE API_ACCOUNT
(
    ID               NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    USERNAME         VARCHAR2(255),
    PASSWORD         VARCHAR2(255),
    NICKNAME         VARCHAR2(255),
    EMAIL            VARCHAR2(255),
    CREATE_DATE_TIME TIMESTAMP,
    UPDATE_DATE_TIME TIMESTAMP,
    PRIMARY KEY (ID)
);

CREATE UNIQUE INDEX IDX_ACCOUNT_1 ON API_ACCOUNT (USERNAME);

-- API_CLIENT表
CREATE TABLE API_CLIENT
(
    ID         VARCHAR2(36) NOT NULL,
    NAME       VARCHAR2(255),
    NOTE       VARCHAR2(255),
    CLIENT_ID  VARCHAR2(255),
    SECRET     VARCHAR2(255),
    EXPIRE_AT  VARCHAR2(255),
    ACCOUNT_ID VARCHAR2(255),
    PRIMARY KEY (ID)
);

CREATE UNIQUE INDEX IDX_API_CLIENT_1 ON API_CLIENT (CLIENT_ID);

-- API_DATASOURCE表
CREATE TABLE API_DATASOURCE
(
    ID         VARCHAR2(36) NOT NULL,
    NAME       VARCHAR2(255),
    NOTE       VARCHAR2(255),
    URL        VARCHAR2(255),
    USERNAME   VARCHAR2(255),
    PASSWORD   VARCHAR2(255),
    TYPE       VARCHAR2(255),
    ACCOUNT_ID VARCHAR2(255),
    PRIMARY KEY (ID)
);

CREATE UNIQUE INDEX IDX_DB_DATASOURCE_1 ON API_DATASOURCE (NAME);

-- API_CONFIG表
CREATE TABLE API_CONFIG
(
    ID            VARCHAR2(36) NOT NULL,
    NAME          VARCHAR2(255),
    NOTE          VARCHAR2(255),
    PATH          VARCHAR2(255),
    DATASOURCE_ID VARCHAR2(36),
    SQL_PARAM     CLOB,
    STATUS        NUMBER,
    ACCOUNT_ID    VARCHAR2(255),
    PRIMARY KEY (ID),
    FOREIGN KEY (DATASOURCE_ID) REFERENCES API_DATASOURCE (ID)
);

CREATE UNIQUE INDEX IDX_API_CONFIG_1 ON API_CONFIG (PATH);

-- API_FIELD_MAPPING表
CREATE TABLE API_FIELD_MAPPING
(
    ID            VARCHAR2(36) NOT NULL,
    API_CONFIG_ID VARCHAR2(36) NOT NULL,
    FIELD_NAME    VARCHAR2(100) NOT NULL,
    DISPLAY_NAME  VARCHAR2(200) NOT NULL,
    COLUMN_WIDTH  NUMBER,
    PRIMARY KEY (ID),
    FOREIGN KEY (API_CONFIG_ID) REFERENCES API_CONFIG (ID) ON DELETE CASCADE
);

CREATE INDEX IDX_API_FIELD_MAPPING_1 ON API_FIELD_MAPPING (API_CONFIG_ID);

-- API_CONFIG_ACCESS表
CREATE TABLE API_CONFIG_ACCESS
(
    ID            VARCHAR2(36) NOT NULL,
    CLIENT_ID     VARCHAR2(36),
    API_CONFIG_ID VARCHAR2(36),
    ACCOUNT_ID    VARCHAR2(255),
    CREATE_TIME   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (ID),
    FOREIGN KEY (CLIENT_ID) REFERENCES API_CLIENT (ID) ON DELETE CASCADE,
    FOREIGN KEY (API_CONFIG_ID) REFERENCES API_CONFIG (ID) ON DELETE CASCADE
);

CREATE UNIQUE INDEX IDX_API_CONFIG_ACCESS_1 ON API_CONFIG_ACCESS (CLIENT_ID, API_CONFIG_ID);

